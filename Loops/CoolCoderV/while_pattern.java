import java.util.Scanner;
public class while_pattern {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.println("Enter the number of rows: ");
        int n = sc.nextInt();
        int i = 1;
        while(n>=i){
            System.out.println("****");
            i++;
        }
        
    }
    
}
