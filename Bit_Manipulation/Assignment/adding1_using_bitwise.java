public class adding1_using_bitwise {
    public static void main(String[] args) {
        int x = 6;
        System.out.println(x + " + 1 is " + -~x);

        int y = -4;
        System.out.println(y + " + 1 is " + -~y);

        int z = 0;
        System.out.println(z + " + 1 is " + -~z);


    }
}


